#define _CRT_SECURE_NO_WARNINGS 1

#include <stdio.h>

int main(void) {

	// 计算机内部二进制数的存储都是以补码的形式存储;
	// 正数(最高位为0):原码, 反码, 补码相同;
	//		例: 5
	//			源码:00000000000000000000000000000101
	//			反码:00000000000000000000000000000101
	//			补码:00000000000000000000000000000101
	// 负数(最高位为1):反码为原码按位以次取反(符号为不变), 补码为反码加1;
	//		例:-6
	//			源码:10000000000000000000000000000110
	//			反码:11111111111111111111111111111001
	//			补码:11111111111111111111111111111010

	//-6+6 = 0
	//11111111111111111111111111111010
	//00000000000000000000000000000110
	//00000000000000000000000000000000

	// 位移操作都是操作整数且位移一个正整数;
	// 左移位操作:	相当于每左移一位乘2
	int a = 8;			// 00000000000000000000000000001000  << 3
	int b = a << 3;		// 00000000000000000000000001000000	 8*2*2*2 = 64
	printf("%d\n", b);

	// 右移位操作:	相当于每右移一位除2
	int m = 8;			// 00000000000000000000000000001000  >> 3
	int n = m >> 2;		// 00000000000000000000000000000010  8/2/2 = 2
	printf("%d\n", n);
	
	// 位运算,都是针对二进制数操作的
	// 按位与&
	// 同为1时为1,其他为0
	int a1 = 5, a2 = 6;
	int a3 = a1 & a2;
	/*
	a1=5		00000101
	a2=6        00000110
	a1&a2       00000100 ---> 4
	*/
	printf("%d\n", a3);

	// 按位或|
	// 同为0时为0,其他为1
	int b1 = 5, b2 = 7;
	int b3 = b1 | b2;
	/*
	b1=5		00000101
	b2=7		00000111
	b1|b2       00000111 ---> 7
	*/
	printf("%d\n", b3);

	// 按位异或^
	// 相同为0,不同为1
	int c1 = 6, c2 = 9;
	int c3 = c1 ^ c2;
	/*
	c1=6       00000110
	c2=9	   00001001
	c1^c2	   00001111 ----> 15
	*/
	printf("%d\n", c3);

	return 0;
}